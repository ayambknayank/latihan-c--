#include <iostream>
#include <cmath> // untuk sqrt
using namespace std;

int main() {
    double x = 16.0;
    double root = sqrt(x);

    cout << "Akar kuadrat dari " << x << " adalah " << root << endl;

    // Contoh lain
    double y = 20.5;
    cout << "Akar kuadrat dari " << y << " adalah " << sqrt(y) << endl;

    return 0;
}
