#include <iostream>

using namespace std;

int main (){
	int a = 100;
	int b = 58 ;
	
	
	//sebanding (==)
	cout  << (a == b) << endl;
	//tidak sebanding (!=)
	cout << (a != b) << endl;
	
	//kurang dari (<)
	cout << (a < b) << endl;
	//lebih dari (>)
	cout << (a > b) << endl;
	
	//kurang dari sama dengan (<=)
	cout << (a <= b) << endl;
	//lebih dari dengan (>=)
	cout << (a >= b) << endl;

}