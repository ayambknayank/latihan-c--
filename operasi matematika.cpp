#include <iostream>

using namespace std;

int main(){
	  float a = 6;
	  float b = 4;
	   // operatornya +, -, *, /, %
	   
	   cout << a << " + " << b << " sama dengan " << a + b << endl;
	   
	   cout << a <<  " - " << b << " sama dengan " << a-b << endl;
	   
	   cout << a <<  " * " << b << " sama dengan " << a*b << endl;
	   
	   //cout << a <<  " % " << b << " sama dengan " << a%b << endl;
	   
	   cout << a <<  " / " << b << " sama dengan " << a/b << endl;
	   
	   cout << (a+b*b) << endl;
}